<section>
    <h3>Tetap Aktif dan Produktif di Masa Pandemi </h3>
    <img src="main/img/jadwal.jpg" alt="" width="30%" style="margin-left: 30%;">
    <p> <strong> 1. Membuat Jadwal Harian</strong> <br>

        Buat jadwal harian atau daftar pekerjaan yang harus diselesaikan setiap hari. Hal ini akan memudahkan untuk fokus juga dapat digunakan sebagai panduan mana pekerjaan yang sudah dikerjakan dan mana pekerjaan yang harus segera diselesaikan agar tidak ada pekerjaan yang terlewat.</p><br>
    
    <img src="main/img/raker.jpg" alt="" width="30%" style="margin-left: 30%;">
    <p><strong>2. Menentukan Ruang Kerja</strong><br>
        Agar saat bekerja tetap nyaman dan tidak terganggu, maka pilihlah salah satu sudut ruangan/rumah sebagai ruang kerja secara khusus. Usahakan untuk menggunakan kursi dan meja saat bekerja agar tidak mudah mengantuk atau malas dalam melakukan aktivitas.
    </p>

    <img src="main/img/disiplin.jpg" alt="" width="30%" style="margin-left: 30%;">
    <p><strong>3. Disiplin dengan Waktu</strong><br>
        Bekerja dari rumah tentunya memberikan jam kerja yang lebih fleksibel.Akan tetapi karena tidak ada pengawasan langsung dari atasan maupun bagian kepatuhan internal, maka pastikan kita bisa disiplin waktu dalam bekerja. Jangan sampai sering meninggalkan pekerjaan untuk urusan lain yang akhirnya menghambat penyelesaian tanggung jawab pekerjaan
    </p>
    <img src="main/img/nohp.jpg" alt="" width="20%" style="margin-left: 40%;">
    <p><strong>4. Jangan Banyak Menggunakan Handphone</strong><br>
      Handphone mungkin akan menjadi salah satu sumber godaan tersendiri saat melakukan pekerjaan di rumah. Maka dari itu kita harus berusaha untuk mengendalikan diri dalam menggunakan handphone. Gunakan handphone sewajarnya pada saat jam kerja.
    </p>

    <img src="main/img/asupan.jpg" alt="" width="30%" style="margin-left: 30%;">
    <p><strong>5. Menghirup Udara Segar dan Menjaga Asupan Makan</strong><br>
      Sekedar menghirup udara segar untuk pergi ke halaman depan atau belakang rumah dapat membantu menghilangkan kebosanan dengan suasana kerja yang mungkin sedikit monoton. Menjaga asupan makan juga sangat penting untuk menjaga kesehatan. Pastikan sarapan yang baik dan jadwalkan istirahat makan siang sesuai jam istirahat di kantor.
    </p>

    <img src="main/img/olahraga.png" alt="" width="30%" style="margin-left: 30%;">
    <p><strong>6. Berolahraga</strong><br>
      Walaupun saat ini sedang pandemi jangan lupa untuk menyempatkan diri berolahraga. Olahraga sangat dibutuhkan untuk menjaga kesehatan dan meningkatkan imunitas tubuh.Semoga tips ini dapat membantu kita untuk tetap aktif, dan dan produktif dalam bekerja.
    </p>
